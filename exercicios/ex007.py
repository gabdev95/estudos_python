# Atividade de média: pega duas notas e faz a média aritmética do aluno.
nota1 = float(input('Digite a nota da 1ª avaliação: '))
nota2 = float(input('Digite a nota da 2ª avaliação: '))
media = (nota1 + nota2)/2
print('A nota do aluno na 1ª avaliação foi: {} \nA nota do aluno na 2ª avaliação foi: {} \nA média da unidade ficou: {}.'.format(nota1, nota2, media))
