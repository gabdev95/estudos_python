n1 = int(input('Digite um valor: '))
n2 = int(input('Digite outro valor: '))
s = n1 + n2
sb = n1 - n2
m = n1 * n2
d = n1 / n2
e = n1 ** n2
di = n1 // n2
rd = n1 % n2
print('A partir dos números digitados o resultado para as seguintes operações são: \nsoma = {}; \nsubtração = {}; \nmultiplicação = {}; \ndivisão = {}; \nexponenciação = {}; \ndivisão inteira = {}; \nresto da divisão = {}.'.format(s, sb, m, d, e, di, rd))
